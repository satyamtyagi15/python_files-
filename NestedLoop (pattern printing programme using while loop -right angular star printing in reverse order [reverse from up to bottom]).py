#Q4-Printing Stars In Reverse Right Angled Triangular Shape.
i=int(input('Enter The No Of Rows You Want:'));
while i>0:
    j=1;
    while j<=i:
        print('*',end='');
        j=j+1;
    print();
    i=i-1;
