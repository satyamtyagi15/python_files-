# programme to print only even numbers from 1 to n.
a=int(input('ENTER THE NUMBER UPTO WHICH YOU WANT TO FIND SUM OF EVEN NUMBERS.'));
i=2;
while i<=a:
    if i%2==0:
        print(i);
    i=i+1;

